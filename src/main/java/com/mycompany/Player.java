//GETTERS AND SETTERS METHOD
public class Player {
    int jerseyno;
    String player_name;
    int player_age;
    String player_type;
    int total_runs;
    int total_wickets;

    public int getJerseyno() {
        return jerseyno;
    }

    public void setJerseyno(int jerseyno) {
        this.jerseyno = jerseyno;
    }

    public String getPlayer_name() {
        return player_name;
    }

    public void setPlayer_name(String player_name) {
        this.player_name = player_name;
    }

    public int getPlayer_age() {
        return player_age;
    }

    public void setPlayer_age(int player_age) {
        this.player_age = player_age;
    }

    public String getPlayer_type() {
        return player_type;
    }

    public void setPlayer_type(String player_type) {
        this.player_type = player_type;
    }

    public int getTotal_runs() {
        return total_runs;
    }

    public void setTotal_runs(int total_runs) {
        this.total_runs = total_runs;
    }

    public int getTotal_wickets() {
        return total_wickets;
    }

    public void setTotal_wickets(int total_wickets) {
        this.total_wickets = total_wickets;
    }
    
    
}
